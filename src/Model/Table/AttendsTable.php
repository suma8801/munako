<?php
declare(strict_types=1);

namespace App\Model\Table;

use Cake\ORM\Query\SelectQuery;
use Cake\ORM\RulesChecker;
use Cake\ORM\Table;
use Cake\Validation\Validator;

/**
 * Attends Model
 *
 * @property \App\Model\Table\MembersTable&\Cake\ORM\Association\BelongsTo $Members
 *
 * @method \App\Model\Entity\Attend newEmptyEntity()
 * @method \App\Model\Entity\Attend newEntity(array $data, array $options = [])
 * @method array<\App\Model\Entity\Attend> newEntities(array $data, array $options = [])
 * @method \App\Model\Entity\Attend get(mixed $primaryKey, array|string $finder = 'all', \Psr\SimpleCache\CacheInterface|string|null $cache = null, \Closure|string|null $cacheKey = null, mixed ...$args)
 * @method \App\Model\Entity\Attend findOrCreate($search, ?callable $callback = null, array $options = [])
 * @method \App\Model\Entity\Attend patchEntity(\Cake\Datasource\EntityInterface $entity, array $data, array $options = [])
 * @method array<\App\Model\Entity\Attend> patchEntities(iterable $entities, array $data, array $options = [])
 * @method \App\Model\Entity\Attend|false save(\Cake\Datasource\EntityInterface $entity, array $options = [])
 * @method \App\Model\Entity\Attend saveOrFail(\Cake\Datasource\EntityInterface $entity, array $options = [])
 * @method iterable<\App\Model\Entity\Attend>|\Cake\Datasource\ResultSetInterface<\App\Model\Entity\Attend>|false saveMany(iterable $entities, array $options = [])
 * @method iterable<\App\Model\Entity\Attend>|\Cake\Datasource\ResultSetInterface<\App\Model\Entity\Attend> saveManyOrFail(iterable $entities, array $options = [])
 * @method iterable<\App\Model\Entity\Attend>|\Cake\Datasource\ResultSetInterface<\App\Model\Entity\Attend>|false deleteMany(iterable $entities, array $options = [])
 * @method iterable<\App\Model\Entity\Attend>|\Cake\Datasource\ResultSetInterface<\App\Model\Entity\Attend> deleteManyOrFail(iterable $entities, array $options = [])
 *
 * @mixin \Cake\ORM\Behavior\TimestampBehavior
 */
class AttendsTable extends Table
{
    /**
     * Initialize method
     *
     * @param array<string, mixed> $config The configuration for the Table.
     * @return void
     */
    public function initialize(array $config): void
    {
        parent::initialize($config);

        $this->setTable('attends');
        $this->setDisplayField('id');
        $this->setPrimaryKey('id');

        $this->addBehavior('Timestamp');

        $this->belongsTo('Members', [
            'foreignKey' => 'member_id',
            'joinType' => 'INNER',
        ]);
    }

    /**
     * Default validation rules.
     *
     * @param \Cake\Validation\Validator $validator Validator instance.
     * @return \Cake\Validation\Validator
     */
    public function validationDefault(Validator $validator): Validator
    {
        $validator
            ->integer('member_id')
            ->notEmptyString('member_id');

        $validator
            ->integer('attend_year')
            ->requirePresence('attend_year', 'create')
            ->notEmptyString('attend_year');

        $validator
            ->scalar('note')
            ->allowEmptyString('note');

        return $validator;
    }

    /**
     * Returns a rules checker object that will be used for validating
     * application integrity.
     *
     * @param \Cake\ORM\RulesChecker $rules The rules object to be modified.
     * @return \Cake\ORM\RulesChecker
     */
    public function buildRules(RulesChecker $rules): RulesChecker
    {
        $rules->add($rules->existsIn(['member_id'], 'Members'), ['errorField' => 'member_id']);

        return $rules;
    }
}
